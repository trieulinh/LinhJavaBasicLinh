package Method;

public class For01 {
    public static void main(String[] args) {
        for (int z = 1; z <= 2; z++) {
            System.out.println("Số hạng thứ nhất: " + z);
            for (int a = 1; a <= 2; a++) {
                System.out.println("Số hạng thứ hai: " + a);
            }
        }
    }}


